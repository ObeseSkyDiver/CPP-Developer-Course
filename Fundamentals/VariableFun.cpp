#include <iostream>

using namespace std;

int main()
{

    // learning about variable types with C++.

    //int is short for integers which are whole numbers. No decimals are in integers.
    int myInt = 15;

    //double is a different type of variable which can include decimals.
    double myDouble = 3.141592653;

    cout << "Integer is " << myInt << "." << endl;
    cout << "The double is " << myDouble << "." << endl;

    return 0;
}
